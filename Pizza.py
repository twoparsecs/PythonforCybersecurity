# Write a script that prompts the user for their favorite topping.

topping = input("What is your favorite pizza topping? ")

if topping == "Peporoni":
    print("Ahh! The classic. Great Choice.")
elif topping == "Jalepeno":
    print("I see you like it spicy.")
elif topping == "Black Olives":
    print("Nice choice.")
elif topping == "Anchovies":
    print("Kow-a-bunga dude!")
elif topping == "Pineapple":
    print("This does not belong on pizza!")
else:
    print("Great option!!!")